    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(CatId.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Enter the Categories to be a deleted");
        }else{
            try{
               Con=DriverManager.getConnection("jdbc:derby://localhost:1527/SuperMarket","User1","ABC");
               int Cid=Integer.valueOf(CatId.getText());
               Statement add=Con.createStatement();
               String Query="Delete from User1.CATEGORIESTB where CATID="+Cid;
               add.executeUpdate(Query);
               SelectCat();
               JOptionPane.showMessageDialog(this, "Categorie deleted Successefully");
            }catch(Exception e){
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_DeleteBtnMouseClicked

    private void CatTBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CatTBMouseClicked
        DefaultTableModel model=(DefaultTableModel)CatTB.getModel();
        int CatIndex=CatTB.getSelectedRow();
        CatId.setText(model.getValueAt(CatIndex, 0).toString());
        CatName.setText(model.getValueAt(CatIndex, 1).toString());
        CatDesc.setText(model.getValueAt(CatIndex, 2).toString());
        
    }//GEN-LAST:event_CatTBMouseClicked

    private void EditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditBtnMouseClicked
        if(CatId.getText().isEmpty()||CatName.getText().isEmpty()||CatDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Imformation");
            
        }else{
            try{
                Con=DriverManager.getConnection("jdbc:derby://localhost:1527/SuperMarket","User1","ABC");
                String query="Update User1.CATEGORIESTB set CATNAME='"+CatName.getText()+"'"+",CATDESC='"+CatDesc.getText()+"'"+"where CATID="+CatId.getText();
                Statement add=Con.createStatement();
                add.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Categorie Updated");
                SelectCat();
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_EditBtnMouseClicked

