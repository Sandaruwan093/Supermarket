    private void CatAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CatAddActionPerformed
        if(CatId.getText().isEmpty()||CatName.getText().isEmpty()||CatDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information");
        }else{
            try{
                Con=DriverManager.getConnection("jdbc:derby://localhost:1527/SuperMarket","User1","ABC");
                PreparedStatement  add=Con.prepareStatement("insert into CATEGORIESTB values(?,?,?)");
                add.setInt(1, Integer.valueOf(CatId.getText()));
                add.setString(2, CatName.getText());
                add.setString(3, CatDesc.getText());
                int row=add.executeUpdate();
                JOptionPane.showMessageDialog(this,"Categories added Successfully");
                Con.close();
                SelectCat();
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_CatAddActionPerformed

